//operadores de comparação
var num1 = "4";
var num2 = 4;

//comparação "==="
var teste = num1 === num2;
console.log(teste);

//iguadade "=="
var teste = num1 == num2;
console.log(teste);

//maior ou igual/ menor ou igual
var teste = num1 >= num2;
console.log(teste);
//ou
var teste = num1 <= num2;
console.log(teste);

